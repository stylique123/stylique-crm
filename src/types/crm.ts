export function getTaskPriority(task: DealTask): TaskPriority {
  if (task.priority) return task.priority;
  const now = Date.now();
  const due = new Date(task.dueDate).getTime();
  if (due < now) return 'critical';
  const hoursUntil = (due - now) / (1000 * 60 * 60);
  if (hoursUntil < 24) return 'high';
  if (hoursUntil < 72) return 'medium';
  return 'low';
}

export function getTaskUrgencyLabel(task: DealTask): { label: string; color: string } {
  const p = getTaskPriority(task);
  switch (p) {
    case 'critical': return { label: 'Do now', color: 'text-destructive' };
    case 'high': return { label: 'Due today', color: 'text-foreground' };
    case 'medium': return { label: 'Next step', color: 'text-muted-foreground' };
    case 'low': return { label: 'Later', color: 'text-muted-foreground/60' };
  }
}

