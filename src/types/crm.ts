// ─── Escalation System ──────────────────────────────────

export interface EscalationInfo {
  level: 'none' | 'warning' | 'overdue' | 'cold';
  message: string;
  daysSinceActivity: number;
}

export function getEscalation(lead: Lead): EscalationInfo {
  if (CONVERTED_STAGES.includes(lead.stage)) {
    if (lead.paymentStatus === 'overdue') {
      const days = getPaymentDaysUntilDue(lead);
      return { level: 'overdue', message: `Payment overdue${days ? ` by ${Math.abs(days)} days` : ''}`, daysSinceActivity: 0 };
    }
    return { level: 'none', message: '', daysSinceActivity: 0 };
  }
  if (CLOSED_STAGES.includes(lead.stage)) {
    return { level: 'none', message: '', daysSinceActivity: 0 };
  }
  if (lead.stage === 'payment-pending') {
    const days = getPaymentDaysUntilDue(lead);
    if (days !== null && days < -3) return { level: 'overdue', message: `Payment ${Math.abs(days)} days overdue`, daysSinceActivity: Math.abs(days) };
    if (days !== null && days < 0) return { level: 'warning', message: `Payment overdue`, daysSinceActivity: Math.abs(days) };
    return { level: 'none', message: '', daysSinceActivity: 0 };
  }
  const now = Date.now();
  const lastActivity = new Date(lead.lastContactedAt || lead.updatedAt).getTime();
  const days = Math.floor((now - lastActivity) / (1000 * 60 * 60 * 24));
  if (TRIAL_STAGES.includes(lead.stage)) {
    const daysLeft = getTrialDaysLeft(lead);
    if (daysLeft !== null && daysLeft <= 0) return { level: 'overdue', message: `Decision overdue`, daysSinceActivity: days };
    if (daysLeft !== null && daysLeft <= 3) return { level: 'warning', message: `Decision due in ${daysLeft} day${daysLeft > 1 ? 's' : ''}`, daysSinceActivity: days };
    return { level: 'none', message: '', daysSinceActivity: days };
  }
  if (days >= 7) return { level: 'cold', message: `No response for ${days} days`, daysSinceActivity: days };
  if (days >= 4) return { level: 'warning', message: `Follow-up due`, daysSinceActivity: days };
  return { level: 'none', message: '', daysSinceActivity: days };
}

// ─── Intelligent Next Actions ────────────────────────────

export interface IntelligentAction {
  action: string;
  reason: string;
  urgency: 'now' | 'today' | 'upcoming';
  followUpDate: string;
  channel?: string;
  deadline?: string;
}

export function recalculateNextAction(lead: Lead): IntelligentAction {
  const now = new Date();
  const addDays = (n: number) => { const d = new Date(now); d.setDate(d.getDate() + n); return d.toISOString(); };
  const stage = normalizeStage(lead.stage);

  switch (stage) {
    case 'sdr-new-lead': case 'new-lead':
      return { action: 'No outreach yet', reason: 'New lead', urgency: 'today', followUpDate: addDays(1), channel: 'email' };

    case 'sdr-contacted': case 'contacted': {
      const contacted = lead.lastContactedAt ? new Date(lead.lastContactedAt) : new Date(lead.updatedAt);
      const daysSince = Math.ceil((now.getTime() - contacted.getTime()) / (1000 * 60 * 60 * 24));
      if (daysSince >= 7) return { action: `No response for ${daysSince} days`, reason: 'No response yet', urgency: 'upcoming', followUpDate: addDays(1) };
      if (daysSince >= 3) return { action: `Follow-up due`, reason: `${daysSince} days since contact`, urgency: 'today', followUpDate: addDays(1), channel: 'phone' };
      return { action: `No response yet`, reason: 'Recently contacted', urgency: 'upcoming', followUpDate: addDays(3 - daysSince) };
    }

    case 'sdr-replied': case 'replied':
      return { action: 'Schedule meeting', reason: 'Reply received', urgency: 'today', followUpDate: addDays(1), channel: 'email' };

    case 'meeting-booked': {
      const meeting = lead.meetingNotes?.[lead.meetingNotes.length - 1];
      if (meeting && new Date(meeting.date) < now && !meeting.summary?.trim()) {
        return { action: 'Meeting result missing', reason: 'Meeting passed without outcome', urgency: 'today', followUpDate: now.toISOString() };
      }
      return { action: 'Meeting scheduled', reason: 'Business conversation scheduled', urgency: 'today', followUpDate: meeting?.date || addDays(1) };
    }

    case 'meeting-completed':
      return { action: 'Awaiting decision', reason: 'Meeting done', urgency: 'today', followUpDate: addDays(1) };

    case 'trial-proposed': {
      const needsApproval = !lead.approvedBy;
      const needsCreds = !hasValidCredentials(lead);
      if (needsApproval && needsCreds) return { action: 'Client Review', reason: 'Approval and credentials pending', urgency: 'today', followUpDate: now.toISOString() };
      if (needsApproval) return { action: 'Client Review', reason: 'Approval pending', urgency: 'today', followUpDate: now.toISOString() };
      if (needsCreds) return { action: 'Credentials missing', reason: 'Credentials pending', urgency: 'today', followUpDate: now.toISOString() };
      if ((lead.paymentStatus !== 'paid' && !lead.paymentReceivedAt) || !lead.paymentVerifiedAt) return { action: 'Verify payment', reason: 'Payment verification pending', urgency: 'today', followUpDate: now.toISOString() };
      return { action: 'Onboarding queue', reason: 'Ready for onboarding', urgency: 'today', followUpDate: addDays(1) };
    }

    case 'trial-active': {
      const daysLeft = getPilotDaysLeft(lead);
      if (daysLeft !== null) {
        if (daysLeft <= 0) return { action: 'Pilot decision due', reason: 'Move to Active Client or Lost', urgency: 'today', followUpDate: now.toISOString() };
        if (daysLeft <= 5) return { action: `Pilot ends in ${daysLeft} day${daysLeft > 1 ? 's' : ''}`, reason: 'Decision due soon', urgency: 'today', followUpDate: now.toISOString() };
      }
      return { action: 'Pilot active', reason: 'Paid pilot running', urgency: 'upcoming', followUpDate: addDays(7) };
    }

    case 'payment-pending': {
      const days = getPaymentDaysUntilDue(lead);
      if (days !== null && days < 0) return { action: `Payment ${Math.abs(days)} days overdue`, reason: 'Overdue', urgency: 'today', followUpDate: now.toISOString() };
      return { action: 'Awaiting payment', reason: 'Payment pending', urgency: 'today', followUpDate: addDays(1) };
    }

    case 'converted':
      return { action: 'Active client', reason: 'Recurring client', urgency: 'upcoming', followUpDate: addDays(30) };

    case 'closed-lost': case 'inbound-disqualified':
      return { action: 'Closed lost', reason: 'Closed', urgency: 'upcoming', followUpDate: addDays(90) };

    // Inbound stages
    case 'inbound-new': case 'new-inquiry':
      return { action: 'New inquiry', reason: 'Inbound lead', urgency: 'today', followUpDate: now.toISOString() };
    case 'inbound-qualified': case 'qualified':
      return { action: 'Schedule meeting', reason: 'Conversation ready', urgency: 'today', followUpDate: addDays(1) };
    case 'inbound-awaiting-sdr': case 'awaiting-sdr':
      return { action: 'No outreach yet', reason: 'Awaiting SDR', urgency: 'today', followUpDate: now.toISOString() };

    default:
      return { action: STAGE_DEFAULT_ACTIONS[lead.stage] || 'Review deal', reason: '', urgency: 'upcoming', followUpDate: addDays(1) };
  }
}

