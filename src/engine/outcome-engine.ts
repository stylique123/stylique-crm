/**
 * STYLIQUE CRM — Outcome Engine
 * 
 * Central intelligence: every outcome maps to a concrete next step.
 * No action completes silently. Every outcome creates the next task automatically.
 * 
 * RULES:
 * 1. Every outcome → exactly one next task or stage change
 * 2. Next task has: title, due date, owner, type, reason
 * 3. Manager-like language in prompts and reasons
 * 4. No dead states — system always knows what's next
 * 5. Old tasks ALWAYS archived on stage change
 * 6. Triggering task ALWAYS completed/archived on outcome
 * 7. Every outcome emits a CRMEvent via the event bus
 */

