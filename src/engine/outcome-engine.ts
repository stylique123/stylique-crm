// ═══════════════════════════════════════════════════════════
// MANAGER PROMPTS — contextual "What happened?" language
// ═══════════════════════════════════════════════════════════

export function getManagerPrompt(actionType: string, companyName: string, contactName: string): string {
  switch (actionType) {
    case 'call': return `You called ${contactName}. What happened?`;
    case 'email': return `You emailed ${contactName}. Did they respond?`;
    case 'linkedin': return `You reached out on LinkedIn. What happened?`;
    case 'meeting': return `Meeting with ${companyName} finished. What was the result?`;
    case 'onboarding-task': return `You worked on ${companyName}'s setup. How did it go?`;
    case 'check-in': return `You checked in on ${companyName}. What did you find?`;
    case 'payment-action': return `Payment follow-up for ${companyName}. What's the status?`;
    case 'trial-action': return `Trial update for ${companyName}. What happened?`;
    case 'follow-up': return `You followed up with ${contactName}. Any response?`;
    default: return `You completed an action for ${companyName}. What happened?`;
  }
}

// ═══════════════════════════════════════════════════════════
// NEXT STEP DESCRIPTIONS — shown in toast after outcome
// ═══════════════════════════════════════════════════════════

export interface OutcomeResult {
  nextStepDescription: string;
  stageChange?: Stage;
  task?: Omit<DealTask, 'id' | 'createdAt'>;
  activityDescription: string;
}

// ═══════════════════════════════════════════════════════════
// CALL OUTCOME → NEXT STEP
// ═══════════════════════════════════════════════════════════

export function processCallOutcome(
  lead: Lead,
  outcome: CallOutcome,
  notes: string,
  performedBy: string,
  bridge: StoreBridge,
  callbackDate?: string,
): OutcomeResult {
  const now = new Date().toISOString();
  const tomorrow = new Date(Date.now() + 86400000).toISOString();
  const in2Days = new Date(Date.now() + 2 * 86400000).toISOString();

  let result: OutcomeResult;

  switch (outcome) {
    case 'interested':
      result = {
        nextStepDescription: `Book meeting with ${lead.contactName} — they're interested`,
        stageChange: lead.stage === 'new-lead' || lead.stage === 'contacted' || lead.stage === 'sdr-contacted' ? 'replied' : undefined,
        task: {
          title: `Book meeting with ${lead.contactName}`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'critical',
          reason: 'Lead expressed interest on call — book meeting today',
          stageFamily: 'engagement',
        },
        activityDescription: `📞 Called ${lead.contactName} — interested. Next: book meeting`,
      };
      break;

    case 'no-answer':
      result = {
        nextStepDescription: `Try ${lead.contactName} again tomorrow via email or LinkedIn`,
        task: {
          title: `Follow up ${lead.companyName} — no answer on call`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'high',
          reason: 'No answer on call — try different channel',
        },
        activityDescription: `📞 Called ${lead.contactName} — no answer`,
      };
      break;

    case 'not-interested':
      result = {
        nextStepDescription: `Close ${lead.companyName} — not interested`,
        stageChange: 'closed-lost',
        activityDescription: `📞 Called ${lead.contactName} — not interested. Deal closed.`,
      };
      break;

    case 'call-back-later':
      result = {
        nextStepDescription: `Callback scheduled for ${lead.contactName}`,
        task: {
          title: `Call back ${lead.contactName}`,
          dueDate: callbackDate ? new Date(callbackDate).toISOString() : in2Days,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'high',
          reason: 'Client asked to call back later',
        },
        activityDescription: `📞 Called ${lead.contactName} — call back later`,
      };
      break;

    case 'answered':
    default:
      result = {
        nextStepDescription: `Continue conversation with ${lead.contactName}`,
        task: {
          title: `Follow up with ${lead.contactName} — continue conversation`,
          dueDate: in2Days,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'medium',
          reason: 'Call connected — follow up to keep momentum',
        },
        activityDescription: `📞 Called ${lead.contactName} — connected${notes ? `: ${notes}` : ''}`,
      };
  }

  // KPI: emit call action
  emitKPI(performedBy, lead, lead.contactName, 'calls_made', 'call', outcome);

  // Apply changes
  applyOutcomeResult(lead, result, performedBy, `Call: ${outcome}`, 'call', notes, bridge);
  return result;
}

// ═══════════════════════════════════════════════════════════
// OUTREACH OUTCOME → NEXT STEP (email/linkedin/apollo)
// ═══════════════════════════════════════════════════════════

export function processOutreachOutcome(
  lead: Lead,
  outcome: OutreachOutcome,
  notes: string,
  performedBy: string,
  channel: 'email' | 'linkedin',
  bridge: StoreBridge,
): OutcomeResult {
  const tomorrow = new Date(Date.now() + 86400000).toISOString();

  let result: OutcomeResult;

  switch (outcome) {
    case 'interested':
      result = {
        nextStepDescription: `Book meeting with ${lead.contactName} now`,
        stageChange: 'replied',
        task: {
          title: `Book meeting with ${lead.contactName}`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'critical',
          reason: 'Lead interested — capitalize immediately',
          stageFamily: 'engagement',
        },
        activityDescription: `✉️ ${channel === 'linkedin' ? 'LinkedIn' : 'Email'} — ${lead.contactName} interested`,
      };
      break;

    case 'replied':
      result = {
        nextStepDescription: `Respond to ${lead.contactName} — keep the conversation going`,
        stageChange: 'replied',
        task: {
          title: `Respond to ${lead.contactName}'s reply`,
          dueDate: new Date().toISOString(), // Today
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'critical',
          reason: 'Lead replied — respond same day',
          stageFamily: 'engagement',
        },
        activityDescription: `✉️ ${lead.contactName} replied via ${channel}`,
      };
      break;

    case 'not-interested':
      result = {
        nextStepDescription: `Close ${lead.companyName} — declined`,
        stageChange: 'closed-lost',
        activityDescription: `✉️ ${lead.contactName} not interested via ${channel}`,
      };
      break;

    case 'no-response':
    default:
      result = {
        nextStepDescription: 'Continue to next outreach step',
        activityDescription: `✉️ ${channel === 'linkedin' ? 'LinkedIn' : 'Email'} sent to ${lead.contactName} — no response yet`,
      };
      break;
  }

  // KPI: emit outreach action
  const kpiMetric = channel === 'email' ? 'emails_sent' : 'linkedin_actions';
  emitKPI(performedBy, lead, lead.contactName, kpiMetric, channel, outcome);
  if (outcome === 'replied' || outcome === 'interested') {
    emitKPI(performedBy, lead, lead.contactName, 'replies_received', channel, outcome);
  }

  applyOutcomeResult(lead, result, performedBy, `${channel}: ${outcome}`, channel, notes, bridge);
  return result;
}

// ═══════════════════════════════════════════════════════════
// MEETING OUTCOME → NEXT STEP
// ═══════════════════════════════════════════════════════════

export function processMeetingOutcome(
  lead: Lead,
  outcome: MeetingOutcome,
  summary: string,
  nextStep: string,
  performedBy: string,
  bridge: StoreBridge,
  nextStepDate?: string,
): OutcomeResult {
  const tomorrow = new Date(Date.now() + 86400000).toISOString();
  const in2Days = new Date(Date.now() + 2 * 86400000).toISOString();

  let result: OutcomeResult;

  switch (outcome) {
    case 'propose_trial':
      result = {
        nextStepDescription: `Moved to Client Review`,
        stageChange: 'trial-proposed',
        task: {
          title: `Client Review — ${lead.companyName}`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'critical',
          reason: 'Moved to Client Review',
          stageFamily: 'trial-prep',
        },
        activityDescription: `Meeting: moved to Client Review for ${lead.companyName}`,
      };
      break;

    case 'interested':
      result = {
        nextStepDescription: nextStep || `Follow up with ${lead.contactName} on discussed items`,
        stageChange: 'meeting-completed',
        task: {
          title: nextStep || `Follow up — ${lead.companyName} interested, propose trial or next steps`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'critical',
          reason: 'Meeting attended — interested, follow up on discussed items',
          stageFamily: 'meeting',
        },
        activityDescription: `🤝 Meeting: ${lead.companyName} — interested${summary ? ` — ${summary}` : ''}`,
      };
      break;

    case 'followup_later':
      result = {
        nextStepDescription: nextStep || `Follow up with ${lead.contactName} later`,
        stageChange: 'meeting-completed',
        task: {
          title: nextStep || `Follow up with ${lead.contactName} — reconnect`,
          dueDate: nextStepDate || in2Days,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'medium',
          reason: 'Follow up later — agreed to reconnect',
          stageFamily: 'meeting',
        },
        activityDescription: `🤝 Meeting: ${lead.companyName} — follow up later${summary ? ` — ${summary}` : ''}`,
      };
      break;

    case 'reschedule':
      result = {
        nextStepDescription: `Meeting rescheduled — prepare again when new date set`,
        task: {
          title: `Confirm rescheduled meeting — ${lead.companyName}`,
          dueDate: in2Days,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'medium',
          reason: 'Meeting rescheduled — confirm new date',
          stageFamily: 'meeting',
        },
        activityDescription: `🤝 Meeting rescheduled: ${lead.companyName}`,
      };
      break;

    case 'no_show':
      result = {
        nextStepDescription: `No-show — call or email ${lead.contactName} now`,
        task: {
          title: `Follow up now — ${lead.contactName} was a no-show`,
          dueDate: new Date().toISOString(),
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'critical',
          reason: 'No-show — call or email now',
          stageFamily: 'meeting',
        },
        activityDescription: `🤝 No-show: ${lead.contactName} missed meeting`,
      };
      break;

    case 'not_fit':
    case 'lost':
      result = {
        nextStepDescription: `Close lead — ${lead.companyName}`,
        stageChange: 'closed-lost',
        activityDescription: `🤝 Meeting: ${lead.companyName} — ${outcome === 'not_fit' ? 'not a fit' : 'lost'}`,
      };
      break;

    case 'pending_internal':
      result = {
        nextStepDescription: `Waiting on internal decision — ${lead.companyName}`,
        stageChange: 'internal-decision',
        task: {
          title: `Follow up on internal decision — ${lead.companyName}`,
          dueDate: nextStepDate || in2Days,
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'high',
          reason: 'Waiting on internal decision — follow up to check progress',
          stageFamily: 'meeting',
        },
        activityDescription: `🤝 Meeting: ${lead.companyName} — waiting on internal decision`,
      };
      break;

    case 'pricing_discussion':
      result = {
        nextStepDescription: `Send pricing proposal to ${lead.contactName}`,
        stageChange: 'pricing-discussion',
        task: {
          title: `Send pricing / proposal — ${lead.companyName}`,
          dueDate: new Date().toISOString(),
          completed: false,
          assignedTo: lead.assignedTo,
          type: 'follow-up',
          autoGenerated: true,
          priority: 'high',
          reason: 'Pricing discussion requested — send proposal today',
          stageFamily: 'meeting',
        },
        activityDescription: `🤝 Meeting: ${lead.companyName} — pricing discussion next`,
      };
      break;

    default:
      result = {
        nextStepDescription: 'Review meeting notes',
        activityDescription: `🤝 Meeting with ${lead.companyName}`,
      };
  }

  // Update meeting notes
  const updated = { ...lead };
  if (updated.meetingNotes?.length) {
    const lastMeeting = { ...updated.meetingNotes[updated.meetingNotes.length - 1] };
    lastMeeting.summary = summary;
    lastMeeting.outcome = outcome;
    lastMeeting.nextStep = nextStep;
    updated.meetingNotes = [...updated.meetingNotes.slice(0, -1), lastMeeting];
  }

  applyOutcomeResult(updated, result, performedBy, `Meeting: ${outcome}`, 'meeting', summary, bridge);

  // KPI: meeting-related emissions
  if (outcome === 'propose_trial') {
    emitKPI(performedBy, lead, lead.contactName, 'trials_proposed', 'meeting', outcome);
  }
  // Every meeting completion counts
  if (['interested', 'propose_trial', 'followup_later', 'pending_internal', 'pricing_discussion', 'not_fit', 'lost'].includes(outcome)) {
    emitKPI(performedBy, lead, lead.contactName, 'meetings_booked', 'meeting', outcome);
  }

  return result;
}

