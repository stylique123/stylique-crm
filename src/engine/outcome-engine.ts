// TASK OUTCOME → NEXT STEP (onboarding, check-in, conversion)
// ═══════════════════════════════════════════════════════════

export function processTaskOutcome(
  lead: Lead,
  outcome: TaskOutcome,
  notes: string,
  taskId: string | undefined,
  taskType: string,
  performedBy: string,
  bridge: StoreBridge,
): OutcomeResult {
  const tomorrow = new Date(Date.now() + 86400000).toISOString();
  const in2Days = new Date(Date.now() + 2 * 86400000).toISOString();
  const in3Days = new Date(Date.now() + 3 * 86400000).toISOString();

  let result: OutcomeResult;

  // Onboarding tasks (Muneeb)
  if (taskType === 'onboarding') {
    switch (outcome) {
      case 'completed':
        result = {
          nextStepDescription: `Setup complete — ${lead.companyName} trial can begin`,
          task: {
            title: `Day 2 check-in — ${lead.companyName}`,
            dueDate: in2Days,
            completed: false,
            assignedTo: 'muneeb',
            type: 'check-in',
            autoGenerated: true,
            priority: 'medium',
            reason: 'Account set up — check if client is using the product',
            stageFamily: 'trial',
          },
          activityDescription: `✅ Setup completed for ${lead.companyName}`,
        };
        break;
      case 'issue':
        result = {
          nextStepDescription: `Setup issue — resolve today for ${lead.companyName}`,
          task: {
            title: `Fix setup issue — ${lead.companyName}`,
            dueDate: new Date().toISOString(),
            completed: false,
            assignedTo: 'muneeb',
            type: 'onboarding',
            autoGenerated: true,
            priority: 'critical',
            reason: `Setup problem: ${notes || 'Issue reported — fix today'}`,
            stageFamily: 'trial',
          },
          activityDescription: `⚠️ Setup issue for ${lead.companyName}${notes ? `: ${notes}` : ''}`,
        };
        break;
      default:
        result = {
          nextStepDescription: 'Next onboarding step queued',
          activityDescription: `Onboarding update: ${lead.companyName} — ${outcome}`,
        };
    }
  }
  // Check-in tasks (Muneeb)
  else if (taskType === 'check-in') {
    switch (outcome) {
      case 'active':
        result = {
          nextStepDescription: `${lead.companyName} is active — schedule next check-in`,
          task: {
            title: `Next check-in — ${lead.companyName}`,
            dueDate: in3Days,
            completed: false,
            assignedTo: 'muneeb',
            type: 'check-in',
            autoGenerated: true,
            priority: 'medium',
            reason: 'Client is using product — periodic check-in',
            stageFamily: 'trial',
          },
          activityDescription: `✅ ${lead.companyName} — active usage confirmed`,
        };
        break;
      case 'not-using':
        result = {
          nextStepDescription: `${lead.companyName} not using — reach out now`,
          task: {
            title: `Call ${lead.contactName} — not using product`,
            dueDate: new Date().toISOString(),
            completed: false,
            assignedTo: 'muneeb',
            type: 'check-in',
            autoGenerated: true,
            priority: 'critical',
            reason: 'Low usage detected — call to help and re-engage',
            stageFamily: 'trial',
          },
          activityDescription: `⚠️ ${lead.companyName} — low/no usage detected`,
        };
        break;
      case 'issue':
        result = {
          nextStepDescription: `${lead.contactName} needs help — support call today`,
          task: {
            title: `Support call with ${lead.contactName}`,
            dueDate: new Date().toISOString(),
            completed: false,
            assignedTo: 'muneeb',
            type: 'check-in',
            autoGenerated: true,
            priority: 'critical',
            reason: `Client needs help: ${notes || 'Questions or issues reported'}`,
            stageFamily: 'trial',
          },
          activityDescription: `📞 ${lead.companyName} needs help${notes ? `: ${notes}` : ''}`,
        };
        break;
      default:
        result = {
          nextStepDescription: 'Next check-in scheduled',
          activityDescription: `Check-in: ${lead.companyName} — ${outcome}`,
        };
    }
  }
  // Conversion push (SDR)
  else if (taskType === 'conversion-push' || taskType === 'trial-end') {
    switch (outcome) {
      case 'interested':
        result = {
          nextStepDescription: `${lead.contactName} wants to convert — send payment details`,
          stageChange: 'payment-pending',
          task: {
            title: `Send payment details to ${lead.contactName}`,
            dueDate: new Date().toISOString(),
            completed: false,
            assignedTo: lead.assignedTo,
            type: 'payment',
            autoGenerated: true,
            priority: 'critical',
            reason: 'Client ready to pay — send details now',
            stageFamily: 'payment',
          },
          activityDescription: `🎯 ${lead.contactName} interested in converting`,
        };
        break;
      case 'not-now':
        result = {
          nextStepDescription: `${lead.contactName} needs time — follow up in 2 days`,
          task: {
            title: `Follow up conversion — ${lead.companyName}`,
            dueDate: in2Days,
            completed: false,
            assignedTo: lead.assignedTo,
            type: 'conversion-push',
            autoGenerated: true,
            priority: 'high',
            reason: 'Client not ready yet — follow up soon',
            stageFamily: 'trial',
          },
          activityDescription: `⏳ ${lead.contactName} — not ready to convert yet`,
        };
        break;
      case 'no-answer':
        result = {
          nextStepDescription: `Try ${lead.contactName} again tomorrow`,
          task: {
            title: `Call ${lead.contactName} again — conversion follow-up`,
            dueDate: tomorrow,
            completed: false,
            assignedTo: lead.assignedTo,
            type: 'conversion-push',
            autoGenerated: true,
            priority: 'high',
            reason: 'Could not reach — try again tomorrow',
            stageFamily: 'trial',
          },
          activityDescription: `📞 ${lead.contactName} — no answer on conversion call`,
        };
        break;
      case 'lost':
        result = {
          nextStepDescription: `${lead.companyName} lost — deal closed`,
          stageChange: 'closed-lost',
          activityDescription: `❌ ${lead.companyName} — will not convert. Deal closed.`,
        };
        break;
      default:
        result = {
          nextStepDescription: 'Next step queued',
          activityDescription: `Conversion update: ${lead.companyName} — ${outcome}`,
        };
    }
  }
  // Generic follow-up — route by stage context
  else {
    switch (outcome) {
      case 'completed':
        result = {
          nextStepDescription: 'Task completed — next step queued',
          activityDescription: `✅ Task completed: ${lead.companyName}`,
        };
        break;
      case 'issue':
        result = {
          nextStepDescription: `Issue found — follow up on ${lead.companyName}`,
          task: {
            title: `Resolve issue — ${lead.companyName}`,
            dueDate: new Date().toISOString(),
            completed: false,
            assignedTo: performedBy,
            type: 'follow-up',
            autoGenerated: true,
            priority: 'high',
            reason: notes || 'Issue reported — resolve today',
          },
          activityDescription: `⚠️ Issue: ${lead.companyName}${notes ? ` — ${notes}` : ''}`,
        };
        break;
      default:
        result = {
          nextStepDescription: 'Next step queued',
          activityDescription: `Task update: ${lead.companyName} — ${outcome}`,
        };
    }
  }

  // Complete the specific task FIRST
  const updated = { ...lead };
  if (taskId) {
    updated.tasks = (updated.tasks || []).map(t =>
      t.id === taskId
        ? { ...t, completed: true, completedAt: new Date().toISOString(), completedBy: performedBy, state: 'completed' as const }
        : t
    );
  }

  applyOutcomeResult(updated, result, performedBy, `${taskType}: ${outcome}`, 'system', notes, bridge);
  return result;
}

// ═══════════════════════════════════════════════════════════
// PAYMENT OUTCOME → NEXT STEP
// ═══════════════════════════════════════════════════════════

export function processPaymentOutcome(
  lead: Lead,
  outcome: PaymentOutcome,
  notes: string,
  performedBy: string,
  bridge: StoreBridge,
): OutcomeResult {
  const in3Days = new Date(Date.now() + 3 * 86400000).toISOString();

  let result: OutcomeResult;

  switch (outcome) {
    case 'paid':
      result = {
        nextStepDescription: `${lead.companyName} is now a paying client!`,
        stageChange: 'converted',
        activityDescription: `💰 Payment received — ${lead.companyName} converted to client`,
      };
      break;
    case 'reminder-sent':
      result = {
        nextStepDescription: `Reminder sent — follow up in 3 days if no payment`,
        task: {
          title: `Follow up payment — ${lead.companyName}`,
          dueDate: in3Days,
          completed: false,
          assignedTo: performedBy,
          type: 'payment',
          autoGenerated: true,
          priority: 'high',
          reason: 'Payment reminder sent — follow up if not received',
          stageFamily: 'payment',
        },
        activityDescription: `📨 Payment reminder sent to ${lead.contactName}`,
      };
      break;
    case 'requested-time':
      result = {
        nextStepDescription: `Client needs more time — check back in 3 days`,
        task: {
          title: `Check payment status — ${lead.companyName}`,
          dueDate: in3Days,
          completed: false,
          assignedTo: performedBy,
          type: 'payment',
          autoGenerated: true,
          priority: 'medium',
          reason: 'Client requested more time — do not let this slip',
          stageFamily: 'payment',
        },
        activityDescription: `⏳ ${lead.contactName} requested more time for payment`,
      };
      break;
    case 'lost':
      result = {
        nextStepDescription: `${lead.companyName} — deal lost`,
        stageChange: 'closed-lost',
        activityDescription: `❌ ${lead.companyName} will not pay — deal closed`,
      };
      break;
    default:
      result = {
        nextStepDescription: 'Payment status updated',
        activityDescription: `Payment update: ${lead.companyName}`,
      };
  }

  // Payment-specific state updates
  const updated = { ...lead };
  if (outcome === 'paid') {
    // Drive monthly billing through the first-class ledger.
    // confirmPaymentAndRoll: marks current entry paid, creates next month
    // entry, advances nextPaymentDate, promotes proposed → active deal.
    const rolled = confirmPaymentAndRoll(updated, performedBy, undefined, notes);
    Object.assign(updated, rolled);
  } else if (outcome === 'reminder-sent') {
    updated.lastContactedAt = new Date().toISOString();
  }

  applyOutcomeResult(updated, result, performedBy, `Payment: ${outcome}`, 'system', notes, bridge);

  // KPI: payment confirmed
  if (outcome === 'paid') {
    emitKPI(performedBy, lead, lead.contactName, 'payments_confirmed', 'system', outcome);
  }

  return result;
}

// ═══════════════════════════════════════════════════════════
// CORE: Apply outcome result to lead — ATOMIC TRANSACTION
// Updates: stage, tasks (archive old + create new), timeline, intelligence
// ═══════════════════════════════════════════════════════════

function applyOutcomeResult(
  lead: Lead,
  result: OutcomeResult,
  performedBy: string,
  actionLabel: string,
  channel: string,
  notes: string,
  bridge: StoreBridge,
  /** Which contact was actually acted upon — defaults to primary */
  targetContactName?: string,
) {
  // Duplicate-submit protection
  if (!allowMutation(lead.id, actionLabel)) {
    console.warn(`[OutcomeEngine] Blocked duplicate: ${actionLabel} on ${lead.companyName}`);
    return;
  }
  const now = new Date().toISOString();
  const updated = { ...lead, updatedAt: now };

  // Mark the ACTUAL contact as reached (not always primary)
  const contactName = targetContactName || lead.contactName;
  if (contactName && ['call', 'email', 'linkedin', 'meeting'].includes(channel)) {
    updated.contacts = markContactReached(updated, contactName);
    updated.contactsReachedCount = (updated.contacts || []).filter(c => c.reached).length;
  }

  // Stage change — archive ALL stale tasks from previous stage
  if (result.stageChange) {
    updated.tasks = archiveStaleTasksForStage(updated.tasks || [], result.stageChange);
    updated.stage = result.stageChange;
  }

  // Cancel any existing auto-generated pending tasks of the SAME type
  // before adding the new one — prevents duplicates.
  if (result.task) {
    updated.tasks = (updated.tasks || []).map(t => {
      if (t.completed || t.state === 'cancelled') return t;
      // Cancel same-type auto tasks to prevent duplicates
      if (t.autoGenerated && t.type === result.task!.type) {
        return {
          ...t,
          state: 'cancelled' as const,
          completed: true,
          cancelledAt: now,
          cancelReason: `Replaced by new task after outcome: ${actionLabel}`,
        };
      }
      return t;
    });

    const newTask: DealTask = {
      ...result.task,
      id: uid(),
      createdAt: now,
      stageFamily: result.task.stageFamily || (result.stageChange
        ? getStageFamily(result.stageChange)
        : getStageFamily(lead.stage)),
    };
    updated.tasks = [...updated.tasks, newTask];
  } else if (!result.stageChange) {
    // No new task AND no stage change — still archive the CURRENT triggering task
    // (the action was completed, so any matching open task should be closed)
    updated.tasks = (updated.tasks || []).map(t => {
      if (t.completed || t.state === 'cancelled') return t;
      // Complete auto tasks matching this action's channel/type
      if (t.autoGenerated && !t.completed) {
        const actionLower = actionLabel.toLowerCase();
        const titleLower = (t.title || '').toLowerCase();
        // Close tasks whose title matches the logged action
        if (
          (actionLower.includes('call') && titleLower.includes('call')) ||
          (actionLower.includes('meeting') && (titleLower.includes('meeting') || titleLower.includes('prep'))) ||
          (actionLower.includes('payment') && titleLower.includes('payment'))
        ) {
          return {
            ...t,
            completed: true,
            completedAt: now,
            completedBy: performedBy,
            state: 'completed' as const,
          };
        }
      }
      return t;
    });
  }

  // Log completion — with contact-level tracking
  updated.actionCompletions = [
    ...(updated.actionCompletions || []),
    { id: uid(), action: actionLabel, completedAt: now, completedBy: performedBy, channel, notes, contactName },
  ];

  // Recalculate intelligence
  const intel = recalculateNextAction(updated);
  updated.nextAction = intel.action;
  updated.nextActionReason = intel.reason;
  updated.nextActionUrgency = intel.urgency;
  updated.nextFollowUp = intel.followUpDate;

  // Persist — ONE atomic save
  bridge.saveCompany(updated);
  bridge.addActivity({
    id: uid(),
    leadId: lead.id,
    type: channel === 'call' ? 'call' : channel === 'email' ? 'email' : channel === 'meeting' ? 'meeting' : 'action-completed',
    description: result.activityDescription,
    createdAt: now,
    createdBy: performedBy,
  });

  // Emit canonical event — all subscribers (toast, KPI dashboards, etc.) react
  // Use specific canonical event types instead of generic legacy types
  const canonicalEventType: CRMEventType = (() => {
    if (channel === 'call') return 'outreach.call_made';
    if (channel === 'meeting') {
      if (result.stageChange === 'trial-proposed') return 'trial.proposed';
      if (result.stageChange === 'internal-decision') return 'lead.stage_changed';
      if (result.stageChange === 'pricing-discussion') return 'lead.stage_changed';
      return 'meeting.outcome_logged';
    }
    if (result.stageChange === 'converted') return 'client.converted';
    if (result.stageChange === 'payment-pending') return 'payment.pending';
    if (result.stageChange === 'closed-lost') return 'lead.closed';
    if (result.stageChange === 'trial-proposed') return 'trial.proposed';
    if (result.stageChange === 'trial-active') return 'trial.activated';
    if (channel === 'email') return 'outreach.email_sent';
    if (channel === 'linkedin') return 'outreach.linkedin_sent';
    return 'lead.stage_changed';
  })();
  emitCRMEvent(canonicalEventType, performedBy, result.activityDescription, {
    leadId: lead.id,
    companyName: lead.companyName,
    contactName: lead.contactName,
    nextStep: result.nextStepDescription,
    metadata: { stageChange: result.stageChange, actionLabel },
  });
}

/** Helper to get stage family for task tagging */
function getStageFamily(stage: string): string {
  const map: Record<string, string> = {
    'new-lead': 'prospecting', 'contacted': 'prospecting', 'lead-added': 'prospecting',
    'sdr-new-lead': 'prospecting', 'sdr-contacted': 'prospecting',
    'outreach-1': 'prospecting', 'outreach-2': 'prospecting', 'outreach-3': 'prospecting',
    'sequence-completed': 'prospecting', 'new-inquiry': 'prospecting',
    'replied': 'engagement', 'sdr-replied': 'engagement',
    'awaiting-sdr': 'engagement', 'qualified': 'engagement',
    'meeting-booked': 'meeting', 'meeting-completed': 'meeting',
    'trial-proposed': 'trial-prep',
    'trial-active': 'trial',
    'payment-pending': 'payment',
    'converted': 'customer',
    'closed-lost': 'closed',
  };
  return map[stage] || 'prospecting';
}
