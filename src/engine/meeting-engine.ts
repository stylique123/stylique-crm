// HELPERS
// ═══════════════════════════════════════════════════════════

const OUTCOME_DESCRIPTIONS: Record<MeetingOutcomeType, string> = {
  interested: 'Interested — continue discussion',
  propose_trial: 'Moved to Client Review',
  followup_later: 'Follow up later',
  not_fit: 'Not a fit — closing',
  lost: 'Lost — deal closed',
  pending_internal: 'Waiting on internal decision',
  pricing_discussion: 'Pricing discussion — send proposal',
  reschedule: 'Rescheduled',
  no_show: 'No show',
};

function isMeetingOutcomeType(value: string | undefined): value is MeetingOutcomeType {
  return !!value && value in OUTCOME_DESCRIPTIONS;
}

function outcomeToStatus(outcome: MeetingOutcomeType): CanonicalMeeting['status'] {
  switch (outcome) {
    case 'reschedule': return 'rescheduled';
    case 'no_show': return 'no_show';
    default: return 'completed';
  }
}

function getOutcomeTransition(
  lead: Lead,
  outcome: MeetingOutcomeType,
  owner: string,
  now: string,
  tomorrow: string,
  in2Days: string,
  nextStepDate?: string,
): { targetStage: Lead['stage']; task: DealTask | null; meetingStatus: Lead['meeting_status'] } {
  switch (outcome) {
    case 'interested':
      return {
        targetStage: 'meeting-completed',
        meetingStatus: 'completed',
        task: {
          id: crypto.randomUUID(),
          title: `Decision pending — ${lead.companyName}`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: owner,
          type: 'follow-up',
          autoGenerated: true,
          createdAt: now,
          priority: 'critical',
          reason: 'Client interested after meeting',
          stageFamily: 'meeting',
        },
      };

    case 'propose_trial':
      return {
        targetStage: 'trial-proposed',
        meetingStatus: 'completed',
        task: {
          id: crypto.randomUUID(),
          title: `Client Review — ${lead.companyName}`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: owner,
          type: 'follow-up',
          autoGenerated: true,
          createdAt: now,
          priority: 'critical',
          reason: 'Moved to Client Review',
          stageFamily: 'trial-prep',
        },
      };

    case 'followup_later':
      return {
        targetStage: 'meeting-completed',
        meetingStatus: 'completed',
        task: {
          id: crypto.randomUUID(),
          title: `Follow up with ${lead.contactName} — reconnect`,
          dueDate: nextStepDate || in2Days,
          completed: false,
          assignedTo: owner,
          type: 'follow-up',
          autoGenerated: true,
          createdAt: now,
          priority: 'medium',
          reason: 'Follow up later — agreed to reconnect',
          stageFamily: 'meeting',
        },
      };

    case 'no_show':
      return {
        targetStage: 'meeting-booked', // stays meeting-booked, needs reschedule
        meetingStatus: 'no-show',
        task: {
          id: crypto.randomUUID(),
          title: `Follow up NOW — ${lead.contactName} was a no-show`,
          dueDate: now,
          completed: false,
          assignedTo: owner,
          type: 'follow-up',
          autoGenerated: true,
          createdAt: now,
          priority: 'critical',
          reason: 'Client did not show up — call or email immediately',
          stageFamily: 'meeting',
        },
      };

    case 'reschedule':
      return {
        targetStage: 'meeting-booked',
        meetingStatus: 'rescheduled',
        task: {
          id: crypto.randomUUID(),
          title: `Confirm rescheduled meeting — ${lead.companyName}`,
          dueDate: tomorrow,
          completed: false,
          assignedTo: owner,
          type: 'follow-up',
          autoGenerated: true,
          createdAt: now,
          priority: 'medium',
          reason: 'Meeting rescheduled — confirm new date',
          stageFamily: 'meeting',
        },
      };

    case 'not_fit':
      return {
        targetStage: 'closed-lost',
        meetingStatus: 'completed',
        task: null,
      };

    case 'lost':
      return {
        targetStage: 'closed-lost',
        meetingStatus: 'completed',
        task: null,
      };

    case 'pending_internal':
      return {
        targetStage: 'meeting-completed',
        meetingStatus: 'completed',
        task: {
          id: crypto.randomUUID(),
          title: `Follow up on internal decision — ${lead.companyName}`,
          dueDate: nextStepDate || in2Days,
          completed: false,
          assignedTo: owner,
          type: 'follow-up',
          autoGenerated: true,
          createdAt: now,
          priority: 'high',
          reason: 'Waiting on internal decision — follow up to check progress',
          stageFamily: 'meeting',
        },
      };
  }
}
