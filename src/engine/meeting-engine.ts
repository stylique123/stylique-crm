/**
 * STYLIQUE CRM — Canonical Meeting Engine
 * 
 * ONE engine for all meeting operations across all surfaces.
 * Every meeting booking, completion, reschedule, and cancel goes through here.
 * 
 * Used by: Pipeline, Tasks, Calendar, CompanyDetailSheet, ExecutionFeed
 * 
 * MEETING OUTCOME ROUTING:
 *   interested        → meeting-completed + follow-up task (propose trial or next steps)
 *   propose_trial     → trial-proposed (enters approval chain)
 *   followup_later    → meeting-completed + dated follow-up task
 *   pending_internal  → meeting-completed + timed follow-up (client deciding)
 *   reschedule        → meeting-booked (stays, new date required)
 *   no_show           → meeting-booked + immediate retry task
 *   not_fit / lost    → closed-lost (archive everything)
 */

import type {
  Lead, CanonicalMeeting, MeetingOutcomeType, DealTask,
  MeetingNote, EntryFlow,
} from '@/types/crm';
import { recalculateNextAction, refreshTasksForStage } from '@/types/crm';
import { archiveStaleTasksForStage } from '@/engine/task-engine';
import type { StoreBridge } from '@/engine/action-executor';

// ═══════════════════════════════════════════════════════════
// BOOK MEETING — creates canonical meeting + updates lead
// ═══════════════════════════════════════════════════════════

export interface BookMeetingOptions {
  lead: Lead;
  meetingType: CanonicalMeeting['meeting_type'];
  meetingLink: string;
  scheduledAt: string;
  meetingSource?: CanonicalMeeting['meeting_source'];
  owner: string;
  performedBy: string;
}

export function bookMeeting(opts: BookMeetingOptions, bridge: StoreBridge): Lead {
  const { lead, meetingType, meetingLink, scheduledAt, meetingSource, owner, performedBy } = opts;
  const now = new Date().toISOString();

  const meeting: CanonicalMeeting = {
    meeting_id: crypto.randomUUID(),
    lead_id: lead.id,
    lead_key: lead.leadKey,
    owner,
    source_flow: lead.entry_flow || 'sdr_manual',
    meeting_type: meetingType,
    meeting_source: meetingSource || 'sdr_booked',
    scheduled_at: scheduledAt,
    meeting_link: meetingLink,
    status: 'scheduled',
    created_at: now,
    updated_at: now,
    sync_status: 'not_configured',
  };

  // Also create legacy MeetingNote for backward compat
  const legacyNote: MeetingNote = {
    id: meeting.meeting_id,
    date: scheduledAt,
    type: meetingType,
    link: meetingLink,
    summary: '',
    attendees: [lead.contactName],
    actionItems: [],
  };

  // Create prep task
  const prepTask: DealTask = {
    id: crypto.randomUUID(),
    title: `Prepare for meeting — ${lead.companyName}`,
    dueDate: scheduledAt,
    completed: false,
    assignedTo: owner,
    type: 'meeting-prep',
    autoGenerated: true,
    createdAt: now,
    priority: 'high',
    reason: `Meeting scheduled ${new Date(scheduledAt).toLocaleDateString()}`,
    stageFamily: 'meeting',
  };

  // Archive all prospecting/engagement tasks — meeting booked is a stage advance
  const archivedTasks = archiveStaleTasksForStage(lead.tasks || [], 'meeting-booked');

  let updated: Lead = {
    ...lead,
    stage: 'meeting-booked',
    meeting_status: 'booked',
    updatedAt: now,
    lastContactedAt: now,
    meetings: [...(lead.meetings || []), meeting],
    meetingNotes: [...(lead.meetingNotes || []), legacyNote],
    tasks: [...archivedTasks, prepTask],
  };

  const intel = recalculateNextAction(updated);
  updated = {
    ...updated,
    nextAction: intel.action,
    nextActionReason: intel.reason,
    nextActionUrgency: intel.urgency,
    nextFollowUp: intel.followUpDate,
  };

  bridge.saveCompany(updated);

  bridge.addActivity({
    id: crypto.randomUUID(),
    leadId: lead.id,
    type: 'meeting_booked',
    description: `📅 Meeting booked: ${lead.companyName} — ${meetingType} on ${new Date(scheduledAt).toLocaleDateString()}`,
    createdAt: now,
    createdBy: performedBy,
  });

  return updated;
}

// ═══════════════════════════════════════════════════════════
// COMPLETE MEETING — process outcome, update state, create next action
// ═══════════════════════════════════════════════════════════

export interface CompleteMeetingOptions {
  lead: Lead;
  meetingId: string;
  outcome: MeetingOutcomeType;
  summary: string;
  notes?: string;
  nextStep?: string;
  nextStepDate?: string;
  performedBy: string;
}

export function completeMeeting(opts: CompleteMeetingOptions, bridge: StoreBridge): Lead {
  const { lead, meetingId, outcome, summary, notes, nextStep, nextStepDate, performedBy } = opts;
  const now = new Date().toISOString();
  const tomorrow = new Date(Date.now() + 86400000).toISOString();
  const in2Days = new Date(Date.now() + 2 * 86400000).toISOString();

  // Update canonical meeting
  const meetings = (lead.meetings || []).map(m =>
    m.meeting_id === meetingId ? {
      ...m,
      status: outcomeToStatus(outcome),
      outcome,
      summary,
      notes,
      next_step: nextStep,
      next_step_date: nextStepDate,
      updated_at: now,
    } : m
  ) as CanonicalMeeting[];

  // Update legacy meetingNotes for backward compat
  const meetingNotes = (lead.meetingNotes || []).map(m =>
    m.id === meetingId ? {
      ...m,
      summary,
      outcome,
      nextStep,
    } : m
  );

  // Determine stage transition and next task based on outcome
  const { targetStage, task, meetingStatus } = getOutcomeTransition(lead, outcome, performedBy, now, tomorrow, in2Days, nextStepDate);

  // Archive ALL stale tasks from the meeting stage — clean slate
  const archivedTasks = archiveStaleTasksForStage(lead.tasks || [], targetStage);

  let updated: Lead = {
    ...lead,
    stage: targetStage,
    meeting_status: meetingStatus,
    updatedAt: now,
    lastContactedAt: now,
    meetings,
    meetingNotes,
    tasks: [
      ...archivedTasks,
      ...(task ? [task] : []),
    ],
  };

  const intel = recalculateNextAction(updated);
  updated = {
    ...updated,
    nextAction: intel.action,
    nextActionReason: intel.reason,
    nextActionUrgency: intel.urgency,
    nextFollowUp: intel.followUpDate,
  };

  bridge.saveCompany(updated);

  bridge.addActivity({
    id: crypto.randomUUID(),
    leadId: lead.id,
    type: 'meeting_completed',
    description: `🤝 Meeting outcome: ${OUTCOME_DESCRIPTIONS[outcome]}${summary ? ` — "${summary}"` : ''}`,
    createdAt: now,
    createdBy: performedBy,
    metadata: { outcome },
  });

  return updated;
}

