// RESCHEDULE MEETING
// ═══════════════════════════════════════════════════════════

export function rescheduleMeeting(
  lead: Lead,
  meetingId: string,
  newScheduledAt: string,
  newLink: string | undefined,
  performedBy: string,
  bridge: StoreBridge,
): Lead {
  const now = new Date().toISOString();

  const meetings = (lead.meetings || []).map(m =>
    m.meeting_id === meetingId ? {
      ...m,
      status: 'rescheduled' as const,
      scheduled_at: newScheduledAt,
      meeting_link: newLink || m.meeting_link,
      updated_at: now,
    } : m
  ) as CanonicalMeeting[];

  // Create new scheduled meeting
  const oldMeeting = lead.meetings?.find(m => m.meeting_id === meetingId);
  const newMeeting: CanonicalMeeting = {
    meeting_id: crypto.randomUUID(),
    lead_id: lead.id,
    lead_key: lead.leadKey,
    owner: oldMeeting?.owner || lead.assignedTo,
    source_flow: lead.entry_flow || 'sdr_manual',
    meeting_type: oldMeeting?.meeting_type || 'zoom',
    meeting_source: oldMeeting?.meeting_source || 'sdr_booked',
    scheduled_at: newScheduledAt,
    meeting_link: newLink || oldMeeting?.meeting_link || '',
    status: 'scheduled',
    created_at: now,
    updated_at: now,
    sync_status: 'not_configured',
  };

  // Legacy compat
  const legacyNote: MeetingNote = {
    id: newMeeting.meeting_id,
    date: newScheduledAt,
    type: newMeeting.meeting_type,
    link: newMeeting.meeting_link,
    summary: '',
    attendees: [lead.contactName],
    actionItems: [],
  };

  // Create new prep task
  const prepTask: DealTask = {
    id: crypto.randomUUID(),
    title: `Prepare for rescheduled meeting — ${lead.companyName}`,
    dueDate: newScheduledAt,
    completed: false,
    assignedTo: oldMeeting?.owner || lead.assignedTo,
    type: 'meeting-prep',
    autoGenerated: true,
    createdAt: now,
    priority: 'high',
    reason: `Meeting rescheduled to ${new Date(newScheduledAt).toLocaleDateString()}`,
    stageFamily: 'meeting',
  };

  // Archive old meeting tasks, keep the new prep
  const archivedTasks = archiveStaleTasksForStage(lead.tasks || [], 'meeting-booked');

  let updated: Lead = {
    ...lead,
    stage: 'meeting-booked',
    meeting_status: 'rescheduled',
    updatedAt: now,
    meetings: [...meetings, newMeeting],
    meetingNotes: [...(lead.meetingNotes || []), legacyNote],
    tasks: [...archivedTasks, prepTask],
  };

  const intel = recalculateNextAction(updated);
  updated = { ...updated, nextAction: intel.action, nextActionReason: intel.reason, nextActionUrgency: intel.urgency, nextFollowUp: intel.followUpDate };

  bridge.saveCompany(updated);

  bridge.addActivity({
    id: crypto.randomUUID(),
    leadId: lead.id,
    type: 'meeting_booked',
    description: `📅 Meeting rescheduled: ${lead.companyName} — ${new Date(newScheduledAt).toLocaleDateString()}`,
    createdAt: now,
    createdBy: performedBy,
  });

  return updated;
}

