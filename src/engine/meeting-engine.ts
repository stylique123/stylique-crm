/** Get all canonical meetings across all leads, role-filtered.
 *  Falls back to legacy meetingNotes when canonical meetings[] is empty. */
export function getCanonicalMeetings(leads: Lead[], viewerRole: string, viewerUser: string): Array<{ meeting: CanonicalMeeting; lead: Lead }> {
  let filtered: Lead[];
  switch (viewerRole) {
    case 'sdr':
      filtered = leads.filter(l => l.assignedTo === viewerUser || l.assigned_sdr === viewerUser);
      break;
    case 'onboarding': {
      // Strict ownership: only this onboarding user's assigned leads.
      // Either the lead is in an onboarding-relevant stage, OR it has
      // an onboarding-sourced meeting THAT this user owns. We never
      // surface another onboarding user's check-ins.
      const onboardingStages = ['trial-proposed', 'trial-active', 'converted'];
      filtered = leads.filter(l => {
        const ownsLead = l.assigned_onboarding_owner === viewerUser;
        if (!ownsLead) return false;
        const inOnboardingStage = onboardingStages.includes(l.stage);
        const hasOnboardingMeeting = (l.meetings || []).some(
          m => m.meeting_source === 'onboarding_checkin'
        );
        return inOnboardingStage || hasOnboardingMeeting;
      });
      break;
    }
    case 'ceo':
    case 'coo':
      filtered = leads;
      break;
    default:
      filtered = [];
  }

  const results: Array<{ meeting: CanonicalMeeting; lead: Lead }> = [];

  for (const l of filtered) {
    // Use canonical meetings if available
    if (l.meetings && l.meetings.length > 0) {
      for (const m of l.meetings) {
        // Fix: past scheduled meetings → outcome required status
        const isPast = new Date(m.scheduled_at) < new Date();
        const corrected = { ...m };
        if (m.status === 'scheduled' && isPast && !m.outcome) {
          // Past meeting still marked scheduled = needs outcome
          corrected.notes = 'Awaiting outcome — add meeting result';
        }
        results.push({ meeting: corrected, lead: l });
      }
    } else if (l.meetingNotes && l.meetingNotes.length > 0) {
      // Fallback: convert legacy meetingNotes to CanonicalMeeting format
      for (const mn of l.meetingNotes) {
        const isPast = new Date(mn.date) < new Date();
        const hasSummary = !!mn.summary?.trim();
        // Past meetings without summary = awaiting outcome, NOT scheduled
        const derivedStatus: CanonicalMeeting['status'] = isPast
          ? (hasSummary ? 'completed' : 'scheduled')
          : 'scheduled';
        results.push({
          meeting: {
            meeting_id: mn.id,
            lead_id: l.id,
            lead_key: l.leadKey,
            owner: l.assignedTo,
            source_flow: l.entry_flow || 'sdr_manual',
            meeting_type: (mn.type as CanonicalMeeting['meeting_type']) || 'zoom',
            meeting_source: 'sdr_booked',
            scheduled_at: mn.date,
            meeting_link: mn.link || '',
            status: derivedStatus,
            outcome: isMeetingOutcomeType(mn.outcome) ? mn.outcome : undefined,
            summary: mn.summary,
            notes: isPast && !hasSummary ? 'Awaiting outcome — add meeting result' : undefined,
            created_at: mn.date,
            updated_at: mn.date,
            sync_status: 'not_configured',
          },
          lead: l,
        });
      }
    }
  }

  return results.sort((a, b) => new Date(a.meeting.scheduled_at).getTime() - new Date(b.meeting.scheduled_at).getTime());
}

/** Find the most recent meeting for a lead that needs outcome */
export function getMeetingNeedingOutcome(lead: Lead): CanonicalMeeting | null {
  const now = new Date();
  return (lead.meetings || [])
    .filter(m => m.status === 'scheduled' && new Date(m.scheduled_at) < now)
    .sort((a, b) => new Date(b.scheduled_at).getTime() - new Date(a.scheduled_at).getTime())[0] || null;
}

/** Find next upcoming meeting for a lead */
export function getNextScheduledMeeting(lead: Lead): CanonicalMeeting | null {
  const now = new Date();
  return (lead.meetings || [])
    .filter(m => m.status === 'scheduled' && new Date(m.scheduled_at) >= now)
    .sort((a, b) => new Date(a.scheduled_at).getTime() - new Date(b.scheduled_at).getTime())[0] || null;
}

